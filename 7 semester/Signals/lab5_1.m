clear;
N = 2^3; % Порядок матриці Адамара
H = hadamard(N) % Матриця Адамара
% Створення сигналу з кількох функцій Уолша-Адамара
y=12.*H(1,:)+10.*H(3,:)+5.*H(5,:)+8.*H(7,:);
plot(y); hold on; % Графік сигналу та його збереження
fwhty = fwht(y,N, 'hadamard') % Коефіцієнти розкладу Уолша-Адамара
plot(ifwht(fwhty,N,'hadamard')); % Сигнал зворотнім перетворенням
hold off;